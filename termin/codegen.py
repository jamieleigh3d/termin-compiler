def _snake(name: str) -> str:
    """Convert a display name to a Python/SQL identifier: 'unit cost' -> 'unit_cost'."""
    return re.sub(r'[^a-z0-9]+', '_', name.lower()).strip('_')


def _table(content_name: str) -> str:
    """Content name to SQL table name: 'stock levels' -> 'stock_levels'."""
    return _snake(content_name)


def _sql_type(te: TypeExpr) -> str:
    """Map a TypeExpr to a SQLite column type + constraints."""
    parts: list[str] = []
    if te.base_type == "text":
        parts.append("TEXT")
    elif te.base_type == "currency":
        parts.append("REAL")
    elif te.base_type == "whole_number":
        parts.append("INTEGER")
    elif te.base_type == "enum":
        vals = ", ".join(f"'{v}'" for v in te.enum_values)
        parts.append(f"TEXT CHECK({{col}} IN ({vals}))")
    elif te.base_type == "reference":
        parts.append("INTEGER")
    elif te.base_type == "automatic":
        parts.append("TIMESTAMP DEFAULT CURRENT_TIMESTAMP")
    else:
        parts.append("TEXT")

    if te.required:
        parts.append("NOT NULL")
    if te.unique:
        parts.append("UNIQUE")
    if te.minimum is not None:
        parts.append(f"CHECK({{col}} >= {te.minimum})")

    return " ".join(parts)


class CodeGenerator:
    def __init__(self, program: Program, source_file: str = "unknown.an AWS-native runtime"):
        self.p = program
        self.source_file = source_file
        self.lines: list[str] = []
        # Map content name -> list of scope->verb mappings for access control
        self._access_map: dict[str, list[AccessRule]] = {}
        for c in self.p.contents:
            self._access_map[c.name] = c.access_rules
        # Map content_name -> StateMachine
        self._state_machines: dict[str, StateMachine] = {}
        for sm in self.p.state_machines:
            self._state_machines[sm.content_name] = sm
        # Collect page names from stories
        self._pages: dict[str, UserStory] = {}
        for story in self.p.stories:
            for d in story.directives:
                if isinstance(d, ShowPage):
                    self._pages[d.page_name] = story

    def generate(self) -> str:
        self._emit_header()
        self._emit_imports()
        self._emit_app_setup()
        self._emit_database()
        self._emit_identity()
        self._emit_event_bus()
        self._emit_models()
        self._emit_state_machines()
        self._emit_event_handlers()
        self._emit_api_routes()
        self._emit_sse_stream()
        self._emit_templates()
        self._emit_page_routes()
        self._emit_startup()
        self._emit_main()
        return "\n".join(self.lines)

    def _w(self, text: str = "") -> None:
        self.lines.append(text)

    def _wblock(self, text: str) -> None:
        self.lines.append(textwrap.dedent(text))

    # ── Header ──

    def _emit_header(self) -> None:
        now = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")
        name = self.p.application.name if self.p.application else "an AWS-native Termin runtime App"
        self._wblock(f'''\
            # ============================================================
            # GENERATED BY TERMIN COMPILER v0.1
            # Source: {self.source_file}
            # Generated: {now}
            # Application: {name}
            # DO NOT EDIT — regenerate from .an AWS-native runtime source
            # ============================================================
        ''')

