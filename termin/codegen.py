    def _emit_transition_route(self, method, path, ep, table, content_name):
        # Infer target state from path suffix
        parts = path.rstrip("/").split("/")
        action = parts[-1]  # "activate", "discontinue", "start", "resolve", etc.

        # Build a map from action words to actual state names using the
        # API endpoint descriptions and the state machine transitions.
        # Common patterns: "activate" -> "active", "discontinue" -> "discontinued"
        # For multi-word states: match the endpoint action to the closest
        # transition target_state in the state machine.
        target_state = action  # fallback
        if content_name in self._state_machines:
            sm = self._state_machines[content_name]
            # Try exact match first
            if action in sm.states:
                target_state = action
            else:
                # Try common verb->state mappings
                verb_map = {
                    "activate": "active",
                    "discontinue": "discontinued",
                    "acknowledge": "acknowledged",
                    "close": "closed",
                    "resolve": "resolved",
                    "reopen": "in progress",
                    "start": "in progress",
                    "wait": "waiting on customer",
                    "complete": "completed",
                    "cancel": "cancelled",
                    "approve": "approved",
                    "reject": "rejected",
                    "archive": "archived",
                    "suspend": "suspended",
                    "resume": "active",
                }
                if action in verb_map and verb_map[action] in sm.states:
                    target_state = verb_map[action]
                else:
                    # Fuzzy match: find states that start with or contain the action
                    for state in sm.states:
                        if state.startswith(action) or action.startswith(state.split()[0]):
                            target_state = state
                            break
                    # Also try matching by the API description
                    if ep and ep.description:
                        desc_lower = ep.description.lower()
                        for state in sm.states:
                            if state in desc_lower:
                                target_state = state
                                break

        param = re.search(r'\{(\w+)\}', path)
        param_name = param.group(1) if param else "id"

        lookup_col = "id"
        content_obj = next((c for c in self.p.contents if c.name == content_name), None)
        if content_obj:
            for f in content_obj.fields:
                if f.type_expr.unique:
                    lookup_col = _snake(f.name)
                    break

        route_name = f"transition_{table}_{_snake(action)}"
        self._w(f'@app.{method}("{path}")')
        self._w(f"async def {route_name}({param_name}: str, user=Depends(get_current_user)):")
        self._w(f'    db = await get_db()')
        self._w(f'    try:')
        self._w(f'        cursor = await db.execute("SELECT id, status FROM {table} WHERE {lookup_col} = ?", ({param_name},))')
        self._w(f'        row = await cursor.fetchone()')
        self._w(f'        if not row:')
        self._w(f'            raise HTTPException(status_code=404, detail="Not found")')
        self._w(f'        return await do_state_transition(db, "{table}", row["id"], "{target_state}", user)')
        self._w(f'    finally:')
        self._w(f'        await db.close()')
        self._w()

