    def _emit_page_route(self, story: UserStory) -> None:
        page_name = None
        display_table = None
        accept_input = None
        filters_directive = None
        search_directive = None
        show_related_directive = None
        aggregations = []
        chart = None

        for d in story.directives:
            if isinstance(d, ShowPage):
                page_name = d.page_name
            elif isinstance(d, DisplayTable):
                display_table = d
            elif isinstance(d, AcceptInput):
                accept_input = d
            elif isinstance(d, AllowFilter):
                filters_directive = d
            elif isinstance(d, AllowSearch):
                search_directive = d
            elif isinstance(d, ShowRelated):
                show_related_directive = d
            elif isinstance(d, DisplayAggregation):
                aggregations.append(d)
            elif isinstance(d, ShowChart):
                chart = d

        if not page_name:
            return

        slug = _snake(page_name)
        var_name = f"TEMPLATE_{slug.upper()}"

        # GET route for the page
        self._w(f'@app.get("/{slug}", response_class=HTMLResponse)')
        self._w(f"async def page_{slug}(request: Request):")
        self._w(f'    user = get_current_user(request)')
        self._w(f'    q = request.query_params.get("q", "")')
        self._w(f'    db = await get_db()')
        self._w(f'    try:')
        self._w(f'        context = {{')
        self._w(f'            "page_title": "{page_name}",')
        self._w(f'            "current_role": user["role"],')
        self._w(f'            "roles": list(ROLES.keys()),')
        self._w(f'            "q": q,')
        self._w(f'        }}')

        if display_table:
            table = _table(display_table.content_name)
            self._w(f'        query = "SELECT * FROM {table}"')
            self._w(f'        params = []')
            self._w(f'        conditions = []')

            # Filter support
            if filters_directive:
                for f in filters_directive.fields:
                    fname = _snake(f)
                    self._w(f'        {fname}_val = request.query_params.get("{fname}", "")')
                    self._w(f'        if {fname}_val:')
                    self._w(f'            conditions.append("{fname} = ?")')
                    self._w(f'            params.append({fname}_val)')

            # Search support
            if search_directive:
                search_cols = [_snake(f) for f in search_directive.fields]
                or_clauses = " OR ".join(f"{c} LIKE ?" for c in search_cols)
                self._w(f'        if q:')
                self._w(f'            conditions.append("({or_clauses})")')
                for _ in search_cols:
                    self._w(f'            params.append(f"%{{q}}%")')

            self._w(f'        if conditions:')
            self._w(f'            query += " WHERE " + " AND ".join(conditions)')
            self._w(f'        cursor = await db.execute(query, params)')
            self._w(f'        rows = await cursor.fetchall()')
            self._w(f'        context["items"] = [dict(r) for r in rows]')
            self._w(f'        context["filters"] = dict(request.query_params)')

            # Populate warehouse_options from distinct values if warehouse filter exists
            if filters_directive and "warehouse" in [_snake(f) for f in filters_directive.fields]:
                self._w(f'        wh_cursor = await db.execute("SELECT DISTINCT warehouse FROM stock_levels ORDER BY warehouse")')
                self._w(f'        context["warehouse_options"] = [r["warehouse"] for r in await wh_cursor.fetchall()]')

            # Show related: for each item, fetch related content
            if show_related_directive:
                related_table = _table(show_related_directive.related_content)
                group_col = _snake(show_related_directive.group_by)
                self._w(f'        for item in context["items"]:')
                self._w(f'            rcursor = await db.execute("SELECT * FROM {related_table} WHERE product = ?", (item["id"],))')
                self._w(f'            related_rows = [dict(r) for r in await rcursor.fetchall()]')
                self._w(f'            item["related"] = ", ".join(str(r.get("{group_col}", "")) + ":" + str(r.get("quantity", "")) for r in related_rows)')

        # Aggregations
        for agg in aggregations:
            slug_agg = _snake(agg.description)[:30]
            if "count" in agg.description.lower():
                # Figure out which content to count
                content_guess = self._guess_content_from_text(agg.description)
                if content_guess:
                    tbl = _table(content_guess)
                    if "active" in agg.description.lower() or "breakdown" in agg.description.lower():
                        self._w(f'        cursor_agg = await db.execute("SELECT COUNT(*) as total, status FROM {tbl} GROUP BY status")')
                        self._w(f'        agg_rows = await cursor_agg.fetchall()')
                        self._w("        agg_rows = [dict(r) for r in agg_rows]")
                        self._w("        breakdown_parts = [str(r['status']) + ': ' + str(r['total']) for r in agg_rows]")
                        self._w("        breakdown = ', '.join(breakdown_parts)")
                        self._w("        total = sum(r['total'] for r in agg_rows)")
                        self._w('        context["' + slug_agg + '"] = str(total) + " (" + breakdown + ")"')
                    else:
                        self._w(f'        cursor_agg = await db.execute("SELECT COUNT(*) as cnt FROM {tbl}")')
                        self._w(f'        context["{slug_agg}"] = (await cursor_agg.fetchone())["cnt"]')
            elif "sum" in agg.description.lower() or "value" in agg.description.lower():
                self._w(f'        cursor_agg = await db.execute("SELECT COALESCE(SUM(sl.quantity * p.unit_cost), 0) as val FROM stock_levels sl JOIN products p ON sl.product = p.id")')
                self._w("        val = (await cursor_agg.fetchone())['val']")
                self._w('        context["' + slug_agg + '"] = f"${val:.2f}"')

        # Nav badges
        if self.p.navigation:
            for item in self.p.navigation.items:
                if item.badge:
                    badge_slug = _snake(item.label)
                    if "alert" in item.badge.lower() and "count" in item.badge.lower():
                        self._w(f'        cursor_badge = await db.execute("SELECT COUNT(*) as cnt FROM reorder_alerts")')
                        self._w(f'        context["badge_{badge_slug}"] = (await cursor_badge.fetchone())["cnt"]')

        # Chart data endpoint
        if chart:
            chart_table = _table(chart.content_name)
            self._w(f'        # Chart data is fetched via separate API endpoint')

        # Fetch reference lists for form dropdowns + existing records for CRUD table
        if accept_input:
            content_obj = self._find_content_for_fields(accept_input.fields)
            if content_obj:
                form_table = _table(content_obj.name)
                for fname in accept_input.fields:
                    col = _snake(fname)
                    field_obj = next((f for f in content_obj.fields if _snake(f.name) == col), None)
                    if field_obj and field_obj.type_expr.references:
                        ref_table = _table(field_obj.type_expr.references)
                        self._w(f'        ref_cursor = await db.execute("SELECT * FROM {ref_table}")')
                        self._w(f'        context["{ref_table}_list"] = [dict(r) for r in await ref_cursor.fetchall()]')

                # Fetch existing records for the auto-generated CRUD table
                has_explicit_table = any(isinstance(dir_, DisplayTable) for dir_ in story.directives)
                if not has_explicit_table:
                    self._w(f'        rows_cursor = await db.execute("SELECT * FROM {form_table} ORDER BY id DESC")')
                    self._w(f'        context["{form_table}_rows"] = [dict(r) for r in await rows_cursor.fetchall()]')

        self._w(f'        content_html = {var_name}.render(**context)')
        self._w(f'        return BASE_TEMPLATE.render(content=content_html, **context)')
        self._w(f'    finally:')
        self._w(f'        await db.close()')
        self._w()

        # POST route for form pages
        if accept_input:
            self._emit_form_post_route(story, accept_input, slug)

    def _emit_form_post_route(self, story, accept_input, slug):
        # Find the content this form creates (best match by field count)
        content_obj = self._find_content_for_fields(accept_input.fields)

        if not content_obj:
            return

        table = _table(content_obj.name)
        create_scope = self._create_scope(content_obj.name)
        scope_dep = f', user=Depends(require_scope("{create_scope}"))' if create_scope else ""

        # Determine initial state (only if content has a state machine)
        initial_state = None
        validate_field = None
        has_state_machine = content_obj.name in self._state_machines
        for d in story.directives:
            if isinstance(d, CreateAs) and has_state_machine:
                initial_state = d.initial_state
            elif isinstance(d, ValidateUnique):
                validate_field = d.field

        form_params = []
        for fname in accept_input.fields:
            col = _snake(fname)
            form_params.append(f'{col}: str = Form("")')

        params_str = ", ".join(form_params)

        self._w(f'@app.post("/{slug}", response_class=HTMLResponse)')
        self._w(f"async def page_{slug}_post(request: Request, edit_id: str = Form(\"\"), {params_str}{scope_dep}):")
        self._w(f'    db = await get_db()')
        self._w(f'    try:')

        # Cast form string values to proper types
        for fname in accept_input.fields:
            col = _snake(fname)
            field_obj = next((f for f in content_obj.fields if _snake(f.name) == col), None)
            if field_obj and field_obj.type_expr.base_type in ("whole_number", "reference"):
                self._w(f'        {col} = int({col}) if {col} else None')
            elif field_obj and field_obj.type_expr.base_type == "currency":
                self._w(f'        {col} = float({col}) if {col} else None')

        self._w(f'        if edit_id:')
        # UPDATE path
        update_cols = [_snake(f) for f in accept_input.fields]
        set_clause = ", ".join(f'{c} = ?' for c in update_cols)
        values_list_update = [_snake(f) for f in accept_input.fields]
        values_str_update = ", ".join(values_list_update)
        self._w(f'            await db.execute(')
        self._w(f'                "UPDATE {table} SET {set_clause} WHERE id = ?",')
        self._w(f'                ({values_str_update}, int(edit_id),)')
        self._w(f'            )')
        self._w(f'        else:')

        # INSERT path
        insert_cols = [_snake(f) for f in accept_input.fields]
        if initial_state:
            insert_cols.append("status")
        cols_sql = ", ".join(insert_cols)
        placeholders = ", ".join(["?"] * len(insert_cols))
        values_list = [_snake(f) for f in accept_input.fields]
        if initial_state:
            values_list.append(f'"{initial_state}"')
        values_str = ", ".join(values_list)

        # Uniqueness validation (only on insert)
        if validate_field:
            vcol = _snake(validate_field)
            self._w(f'            cursor = await db.execute("SELECT id FROM {table} WHERE {vcol} = ?", ({vcol},))')
            self._w(f'            if await cursor.fetchone():')
            self._w(f'                raise HTTPException(status_code=422, detail="{validate_field} already exists")')

        self._w(f'            await db.execute(')
        self._w(f'                "INSERT INTO {table} ({cols_sql}) VALUES ({placeholders})",')
        self._w(f'                ({values_str},)')
        self._w(f'            )')
        self._w(f'        await db.commit()')
        self._w(f'        return RedirectResponse(url="/{slug}", status_code=303)')
        self._w(f'    finally:')
        self._w(f'        await db.close()')
        self._w()

        # DELETE route for this form page
        has_display_table = any(isinstance(dir_, DisplayTable) for dir_ in story.directives)
        if not has_display_table:
            delete_scope = self._delete_scope(content_obj.name)
            update_scope = self._update_scope(content_obj.name)
            # Use the most permissive scope available (update or delete)
            del_scope = delete_scope or update_scope or create_scope
            del_scope_dep = f', user=Depends(require_scope("{del_scope}"))' if del_scope else ""
            self._w(f'@app.post("/{slug}/delete/{{record_id}}", response_class=HTMLResponse)')
            self._w(f"async def page_{slug}_delete(record_id: int{del_scope_dep}):")
            self._w(f'    db = await get_db()')
            self._w(f'    try:')
            self._w(f'        await db.execute("DELETE FROM {table} WHERE id = ?", (record_id,))')
            self._w(f'        await db.commit()')
            self._w(f'        return RedirectResponse(url="/{slug}", status_code=303)')
            self._w(f'    finally:')
            self._w(f'        await db.close()')
            self._w()

    def _find_content_for_fields(self, field_names: list[str]):
        """Find the Content object that best matches the given field names."""
        best = None
        best_count = 0
        for c in self.p.contents:
            count = sum(1 for f in field_names if _snake(f) in {_snake(cf.name) for cf in c.fields})
            if count > best_count:
                best_count = count
                best = c
        return best

    def _guess_content_from_text(self, text: str) -> Optional[str]:
        text_lower = text.lower()
        for c in self.p.contents:
            if c.name.lower() in text_lower or c.singular.lower() in text_lower:
                return c.name
        return None

