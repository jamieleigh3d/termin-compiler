    # ── App Setup ──

    def _emit_app_setup(self) -> None:
        name = self.p.application.name if self.p.application else "an AWS-native Termin runtime App"
        desc = self.p.application.description if self.p.application else ""
        self._wblock(f'''\
            DB_PATH = "app.db"

            @asynccontextmanager
            async def lifespan(app):
                await init_db()
                yield

            app = FastAPI(title="{name}", description="{desc}", lifespan=lifespan)
        ''')

    # ── Database ──

    def _emit_database(self) -> None:
        self._wblock('''\
            async def get_db():
                db = await aiosqlite.connect(DB_PATH)
                db.row_factory = aiosqlite.Row
                await db.execute("PRAGMA foreign_keys = ON")
                return db

            async def init_db():
                db = await get_db()
                try:
        ''')

        for content in self.p.contents:
            table = _table(content.name)
            cols = ["id INTEGER PRIMARY KEY AUTOINCREMENT"]

            # Add status column if this content has a state machine
            if content.name in self._state_machines:
                sm = self._state_machines[content.name]
                cols.append(f"status TEXT NOT NULL DEFAULT '{sm.initial_state}'")

            for field in content.fields:
                col_name = _snake(field.name)
                sql_type = _sql_type(field.type_expr)
                # Replace {col} placeholder with actual column name
                sql_type = sql_type.replace("{col}", col_name)
                col_def = f"{col_name} {sql_type}"
                cols.append(col_def)

            # Foreign keys
            fk_clauses = []
            for field in content.fields:
                if field.type_expr.references:
                    col_name = _snake(field.name)
                    ref_table = _table(field.type_expr.references)
                    fk_clauses.append(
                        f"FOREIGN KEY ({col_name}) REFERENCES {ref_table}(id)"
                    )

            all_cols = cols + fk_clauses
            cols_sql = ",\n                ".join(all_cols)
            self._w(f'        await db.execute("""')
            self._w(f"            CREATE TABLE IF NOT EXISTS {table} (")
            self._w(f"                {cols_sql}")
            self._w(f"            )")
            self._w(f'        """)')
            self._w()

        self._w("        await db.commit()")
        self._w("    finally:")
        self._w("        await db.close()")
        self._w()

    # ── Identity / Auth ──

    def _emit_identity(self) -> None:
        roles_dict = {}
        for role in self.p.roles:
            roles_dict[role.name] = role.scopes

        self._w(f"ROLES = {repr(roles_dict)}")
        self._w()

        # Scope list for reference
        scopes = list(self.p.identity.scopes) if self.p.identity else []
        self._w(f"ALL_SCOPES = {repr(scopes)}")
        self._w()

        self._wblock('''\
            def get_current_user(request: Request) -> dict:
                """Get current user from cookie or default to first role."""
                role = request.cookies.get("termin_role", list(ROLES.keys())[0])
                if role not in ROLES:
                    role = list(ROLES.keys())[0]
                return {"role": role, "scopes": ROLES[role]}

            def require_scope(scope: str):
                """FastAPI dependency that checks the user has a required scope."""
                def checker(request: Request):
                    user = get_current_user(request)
                    if scope not in user["scopes"]:
                        raise HTTPException(status_code=403, detail=f"Requires scope: {scope}")
                    return user
                return checker
        ''')

    # ── Event Bus ──

    def _emit_event_bus(self) -> None:
        self._wblock('''\
            class EventBus:
                def __init__(self):
                    self._subscribers: list[asyncio.Queue] = []

                def subscribe(self) -> asyncio.Queue:
                    q: asyncio.Queue = asyncio.Queue()
                    self._subscribers.append(q)
                    return q

                def unsubscribe(self, q: asyncio.Queue):
                    self._subscribers.remove(q)

                async def publish(self, event: dict):
                    for q in self._subscribers:
                        await q.put(event)

            event_bus = EventBus()
        ''')

    # ── Pydantic Models ──

    def _emit_models(self) -> None:
        for content in self.p.contents:
            class_name = _snake(content.name).title().replace("_", "")
            self._w(f"class {class_name}Create(BaseModel):")
            has_fields = False
            for field in content.fields:
                if field.type_expr.base_type == "automatic":
                    continue
                fname = _snake(field.name)
                ptype = self._pydantic_type(field.type_expr)
                default = "" if field.type_expr.required else " = None"
                self._w(f"    {fname}: {ptype}{default}")
                has_fields = True
            if not has_fields:
                self._w("    pass")

            # Add validator for enum fields
            for field in content.fields:
                if field.type_expr.base_type == "enum":
                    fname = _snake(field.name)
                    vals = repr(field.type_expr.enum_values)
                    self._w()
                    self._w(f'    @field_validator("{fname}")')
                    self._w(f"    @classmethod")
                    self._w(f"    def validate_{fname}(cls, v):")
                    self._w(f"        if v is not None and v not in {vals}:")
                    self._w(f'            raise ValueError(f"{{v}} is not a valid {field.name}")')
                    self._w(f"        return v")

            self._w()

            # Update model (all fields optional)
            update_class = _snake(content.name).title().replace("_", "") + "Update"
            self._w(f"class {update_class}(BaseModel):")
            has_upd_fields = False
            for field in content.fields:
                if field.type_expr.base_type == "automatic":
                    continue
                fname = _snake(field.name)
                ptype = self._pydantic_type_optional(field.type_expr)
                self._w(f"    {fname}: {ptype} = None")
                has_upd_fields = True
            if not has_upd_fields:
                self._w("    pass")
            self._w()

    def _pydantic_type_optional(self, te: TypeExpr) -> str:
        base = self._pydantic_type(te)
        if base.startswith("Optional["):
            return base
        return f"Optional[{base}]"

    def _pydantic_type(self, te: TypeExpr) -> str:
        base = "str"
        if te.base_type == "whole_number":
            base = "int"
        elif te.base_type == "currency":
            base = "float"
        elif te.base_type == "reference":
            base = "int"
        elif te.base_type == "enum":
            base = "str"
        elif te.base_type == "text":
            base = "str"

        if not te.required:
            return f"Optional[{base}]"
        return base

    # ── State Machines ──

    def _emit_state_machines(self) -> None:
        if not self.p.state_machines:
            return

        self._w("# --- State Machine Transition Tables ---")
        self._w()
        self._w("STATE_MACHINES = {")
        for sm in self.p.state_machines:
            table = _table(sm.content_name)
            self._w(f'    "{table}": {{')
            self._w(f'        "initial": "{sm.initial_state}",')
            self._w(f'        "transitions": {{')
            for tr in sm.transitions:
                self._w(f'            ("{tr.from_state}", "{tr.to_state}"): "{tr.required_scope}",')
            self._w(f'        }},')
            self._w(f'    }},')
        self._w("}")
        self._w()

        self._wblock('''\
            async def do_state_transition(db, table: str, record_id: int, target_state: str, user: dict):
                """Attempt a state transition. Raises HTTPException on invalid transition."""
                if table not in STATE_MACHINES:
                    raise HTTPException(status_code=400, detail=f"No state machine for {table}")

                sm = STATE_MACHINES[table]
                cursor = await db.execute(f"SELECT status FROM {table} WHERE id = ?", (record_id,))
                row = await cursor.fetchone()
                if not row:
                    raise HTTPException(status_code=404, detail="Record not found")

                current = row["status"]
                key = (current, target_state)
                if key not in sm["transitions"]:
                    raise HTTPException(
                        status_code=409,
                        detail=f"Cannot transition from '{current}' to '{target_state}'"
                    )

                required_scope = sm["transitions"][key]
                if required_scope not in user["scopes"]:
                    raise HTTPException(
                        status_code=403,
                        detail=f"Transition requires scope: {required_scope}"
                    )

                await db.execute(f"UPDATE {table} SET status = ? WHERE id = ?", (target_state, record_id))
                await db.commit()
                await event_bus.publish({"type": f"{table}_updated", "id": record_id, "status": target_state})
                return {"id": record_id, "status": target_state}
        ''')

    # ── Event Handlers ──

    def _emit_event_handlers(self) -> None:
        if not self.p.events:
            self._w("async def run_event_handlers(db, content_name: str, trigger: str, record: dict):")
            self._w("    pass")
            self._w()
            return

        self._w("async def run_event_handlers(db, content_name: str, trigger: str, record: dict):")
        self._w('    """Execute event rules that match the content mutation."""')

        for event in self.p.events:
            # Event content_name may be singular ("stock level") — find the actual Content
            content_table = _table(event.content_name)
            # Also try the plural form to match against defined Content names
            for c in self.p.contents:
                if _table(c.name) == content_table or _table(c.singular) == content_table:
                    content_table = _table(c.name)
                    break
            self._w(f'    if content_name == "{content_table}" and trigger == "{event.trigger}":')

            if event.condition:
                f1 = _snake(event.condition.field1)
                f2 = _snake(event.condition.field2)
                if event.condition.operator == "at or below":
                    self._w(f'        if record.get("{f1}", 0) <= record.get("{f2}", 0):')
                else:
                    self._w(f'        if True:  # condition: {event.condition.operator}')

                if event.action:
                    # Find the actual Content for the target
                    target_table = _table(event.action.create_content + "s")
                    target_content = None
                    for c in self.p.contents:
                        if c.singular == event.action.create_content or c.name == event.action.create_content:
                            target_table = _table(c.name)
                            target_content = c
                            break

                    # Find source content for field mapping
                    source_content = None
                    for c in self.p.contents:
                        if _table(c.name) == content_table:
                            source_content = c
                            break
                    source_cols = {_snake(f.name) for f in source_content.fields} if source_content else set()

                    # Map event field names to target columns and source record keys
                    target_cols = []
                    source_keys = []
                    for fname in event.action.fields:
                        target_col = _snake(fname)
                        target_cols.append(target_col)
                        # If the target col name exists in the source record, use it directly
                        if target_col in source_cols:
                            source_keys.append(target_col)
                        else:
                            # Try to find a matching source field by partial name
                            matched = False
                            for sc in source_cols:
                                if fname.lower() in sc or sc in _snake(fname):
                                    source_keys.append(sc)
                                    matched = True
                                    break
                            if not matched:
                                source_keys.append(target_col)

                    cols = ", ".join(target_cols)
                    placeholders = ", ".join(["?"] * len(target_cols))
                    values = ", ".join(f'record.get("{sk}", "")' for sk in source_keys)

                    self._w(f'            await db.execute(')
                    self._w(f'                "INSERT INTO {target_table} ({cols}) VALUES ({placeholders})",')
                    self._w(f'                ({values},)')
                    self._w(f'            )')
                    self._w(f'            await db.commit()')
                    self._w(f'            await event_bus.publish({{"type": "{target_table}_created"}})')
            else:
                if event.action:
                    # Find the actual Content for the target
                    target_table = _table(event.action.create_content + "s")
                    for c in self.p.contents:
                        if c.singular == event.action.create_content or c.name == event.action.create_content:
                            target_table = _table(c.name)
                            break
                    self._w(f'        pass  # TODO: unconditional event action')

        self._w()

    # ── API Routes ──

    def _emit_api_routes(self) -> None:
        if not self.p.api:
            return

        base = self.p.api.base_path.rstrip("/")

        for ep in self.p.api.endpoints:
            full_path = base + ep.path
            method = ep.method.lower()

            # Determine which content this endpoint relates to
            content, content_obj = self._infer_content_for_path(ep.path)
            if not content:
                continue

            table = _table(content)

            # Determine the operation type from method + path pattern
            has_param = "{" in ep.path
            is_transition = any(
                word in ep.description.lower()
                for word in ["transition", "activate", "discontinue", "acknowledge"]
            )

            if is_transition:
                self._emit_transition_route(method, full_path, ep, table, content)
            elif method == "get" and not has_param:
                self._emit_list_route(full_path, ep, table, content, content_obj)
            elif method == "get" and has_param:
                self._emit_get_route(full_path, ep, table, content, content_obj)
            elif method == "post" and not has_param:
                self._emit_create_route(full_path, ep, table, content, content_obj)
            elif method == "put" and has_param:
                self._emit_update_route(full_path, ep, table, content, content_obj)
            elif method == "delete" and has_param:
                self._emit_delete_route(full_path, ep, table, content, content_obj)

    def _infer_content_for_path(self, path: str) -> tuple[Optional[str], Optional[Content]]:
        """Infer which Content a path relates to based on the first segment."""
        # /products -> "products", /stock-levels -> "stock levels", /alerts -> "reorder alerts"
        parts = path.strip("/").split("/")
        segment = parts[0] if parts else ""
        segment_norm = segment.replace("-", " ")

        for c in self.p.contents:
            if _snake(c.name) == _snake(segment_norm):
                return c.name, c
            # Try partial match: "alerts" -> "reorder alerts"
            if segment_norm in c.name or c.name.endswith(segment_norm):
                return c.name, c

        return None, None

    def _view_scope(self, content_name: str) -> Optional[str]:
        for rule in self._access_map.get(content_name, []):
            if "view" in rule.verbs:
                return rule.scope
        return None

    def _create_scope(self, content_name: str) -> Optional[str]:
        for rule in self._access_map.get(content_name, []):
            if "create" in rule.verbs or "create or update" in rule.verbs:
                return rule.scope
        return None

    def _update_scope(self, content_name: str) -> Optional[str]:
        for rule in self._access_map.get(content_name, []):
            if "update" in rule.verbs or "create or update" in rule.verbs:
                return rule.scope
        return None

    def _delete_scope(self, content_name: str) -> Optional[str]:
        for rule in self._access_map.get(content_name, []):
            if "delete" in rule.verbs:
                return rule.scope
        return None

    def _emit_list_route(self, path, ep, table, content_name, content_obj):
        scope = self._view_scope(content_name)
        scope_dep = f', user=Depends(require_scope("{scope}"))' if scope else ""
        self._w(f'@app.get("{path}")')
        self._w(f"async def list_{table}(request: Request{scope_dep}):")
        self._w(f'    db = await get_db()')
        self._w(f'    try:')
        self._w(f'        cursor = await db.execute("SELECT * FROM {table}")')
        self._w(f'        rows = await cursor.fetchall()')
        self._w(f'        return [{{"id": r["id"], **dict(r)}} for r in rows]')
        self._w(f'    finally:')
        self._w(f'        await db.close()')
        self._w()

    def _emit_get_route(self, path, ep, table, content_name, content_obj):
        scope = self._view_scope(content_name)
        scope_dep = f', user=Depends(require_scope("{scope}"))' if scope else ""
        # Determine param name from path
        param = re.search(r'\{(\w+)\}', path)
        param_name = param.group(1) if param else "id"

        # Determine lookup column
        lookup_col = "id"
        if content_obj:
            for f in content_obj.fields:
                if f.type_expr.unique:
                    lookup_col = _snake(f.name)
                    break

        self._w(f'@app.get("{path}")')
        self._w(f"async def get_{table}_by_{param_name}({param_name}: str{scope_dep}):")
        self._w(f'    db = await get_db()')
        self._w(f'    try:')
        self._w(f'        cursor = await db.execute("SELECT * FROM {table} WHERE {lookup_col} = ?", ({param_name},))')
        self._w(f'        row = await cursor.fetchone()')
        self._w(f'        if not row:')
        self._w(f'            raise HTTPException(status_code=404, detail="Not found")')
        self._w(f'        return dict(row)')
        self._w(f'    finally:')
        self._w(f'        await db.close()')
        self._w()

    def _emit_create_route(self, path, ep, table, content_name, content_obj):
        scope = self._create_scope(content_name)
        scope_dep = f', user=Depends(require_scope("{scope}"))' if scope else ""
        model_name = _snake(content_name).title().replace("_", "") + "Create"

        self._w(f'@app.post("{path}", status_code=201)')
        self._w(f"async def create_{table}(data: {model_name}{scope_dep}):")
        self._w(f'    db = await get_db()')
        self._w(f'    try:')
        self._w(f'        d = data.model_dump(exclude_none=True)')

        # Add initial state if state machine exists
        if content_name in self._state_machines:
            sm = self._state_machines[content_name]
            self._w(f'        d.setdefault("status", "{sm.initial_state}")')

        cols = ", ".join(f'"{_snake(f.name)}"' for f in content_obj.fields if f.type_expr.base_type != "automatic")
        self._w(f'        columns = [k for k in d.keys()]')
        self._w(f'        placeholders = ", ".join(["?"] * len(columns))')
        self._w(f'        col_str = ", ".join(columns)')
        self._w(f'        values = [d[k] for k in columns]')
        self._w(f'        try:')
        self._w(f'            cursor = await db.execute(f"INSERT INTO {table} ({{col_str}}) VALUES ({{placeholders}})", values)')
        self._w(f'        except sqlite3.IntegrityError as e:')
        self._w(f'            raise HTTPException(status_code=409, detail=str(e))')
        self._w(f'        await db.commit()')
        self._w(f'        record_id = cursor.lastrowid')

        # Fetch created record for event handlers
        self._w(f'        cursor2 = await db.execute("SELECT * FROM {table} WHERE id = ?", (record_id,))')
        self._w(f'        record = dict(await cursor2.fetchone())')
        self._w(f'        await run_event_handlers(db, "{table}", "created", record)')
        self._w(f'        await event_bus.publish({{"type": "{table}_created", "id": record_id}})')
        self._w(f'        return {{"id": record_id, **record}}')
        self._w(f'    finally:')
        self._w(f'        await db.close()')
        self._w()

    def _emit_update_route(self, path, ep, table, content_name, content_obj):
        scope = self._update_scope(content_name)
        scope_dep = f', user=Depends(require_scope("{scope}"))' if scope else ""
        model_name = _snake(content_name).title().replace("_", "") + "Update"
        param = re.search(r'\{(\w+)\}', path)
        param_name = param.group(1) if param else "id"

        lookup_col = "id"
        if content_obj:
            for f in content_obj.fields:
                if f.type_expr.unique:
                    lookup_col = _snake(f.name)
                    break

        self._w(f'@app.put("{path}")')
        self._w(f"async def update_{table}({param_name}: str, data: {model_name}{scope_dep}):")
        self._w(f'    db = await get_db()')
        self._w(f'    try:')
        self._w(f'        d = data.model_dump(exclude_unset=True)')
        self._w(f'        if not d:')
        self._w(f'            raise HTTPException(status_code=422, detail="No fields to update")')
        self._w(f'        set_clause = ", ".join(f"{{k}} = ?" for k in d.keys())')
        self._w(f'        values = list(d.values()) + [{param_name}]')
        self._w(f'        await db.execute(f"UPDATE {table} SET {{set_clause}} WHERE {lookup_col} = ?", values)')
        self._w(f'        await db.commit()')

        # Fetch updated record for event handlers
        self._w(f'        cursor = await db.execute("SELECT * FROM {table} WHERE {lookup_col} = ?", ({param_name},))')
        self._w(f'        row = await cursor.fetchone()')
        self._w(f'        if not row:')
        self._w(f'            raise HTTPException(status_code=404, detail="Not found")')
        self._w(f'        record = dict(row)')
        self._w(f'        await run_event_handlers(db, "{table}", "updated", record)')
        self._w(f'        await event_bus.publish({{"type": "{table}_updated", "id": row["id"]}})')
        self._w(f'        return record')
        self._w(f'    finally:')
        self._w(f'        await db.close()')
        self._w()

