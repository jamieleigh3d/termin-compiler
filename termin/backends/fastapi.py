    def _emit_page_routes(self) -> None:
        # Home route -> redirect to first page
        if self.spec.pages:
            first_slug = self.spec.pages[0].slug
            self._w(f'@app.get("/", response_class=HTMLResponse)')
            self._w(f"async def home(request: Request):")
            self._w(f'    return RedirectResponse(url="/{first_slug}")')
            self._w()

        # Emit one route per unique slug (merged pages share a route)
        emitted_slugs: set[str] = set()
        for page in self.spec.pages:
            if page.slug not in emitted_slugs:
                self._emit_page_route(page)
                emitted_slugs.add(page.slug)

    def _emit_page_route(self, page: PageSpec) -> None:
        slug = page.slug
        var_name = f"TEMPLATE_{slug.upper()}"

        # GET route
        self._w(f'@app.get("/{slug}", response_class=HTMLResponse)')
        self._w(f"async def page_{slug}(request: Request):")
        self._w(f'    user = get_current_user(request)')
        self._w(f'    q = request.query_params.get("q", "")')
        self._w(f'    db = await get_db()')
        self._w(f'    try:')
        self._w(f'        context = {{')
        self._w(f'            "page_title": "{page.name}",')
        self._w(f'            "current_role": user["role"],')
        self._w(f'            "current_user_name": user["profile"]["DisplayName"],')
        self._w(f'            "user_profile_json": json.dumps(user["profile"]),')
        self._w(f'            "roles": list(ROLES.keys()),')
        self._w(f'            "q": q,')
        self._w(f'        }}')

        if page.display_table:
            table = page.display_table
            self._w(f'        query = "SELECT * FROM {table}"')
            self._w(f'        params = []')
            self._w(f'        conditions = []')

            # Filter support — only apply filters for columns on the display table
            table_obj = next((t for t in self.spec.tables if t.name.snake == table), None)
            table_cols = {c.name for c in table_obj.columns} if table_obj else set()
            if table_obj and table_obj.has_status_column:
                table_cols.add("status")
            if page.filters:
                for ff in page.filters:
                    fname = ff.key
                    self._w(f'        {fname}_val = request.query_params.get("{fname}", "")')
                    if fname in table_cols:
                        self._w(f'        if {fname}_val:')
                        self._w(f'            conditions.append("{fname} = ?")')
                        self._w(f'            params.append({fname}_val)')

            # Search support
            if page.search_fields:
                search_cols = list(page.search_fields)
                or_clauses = " OR ".join(f"{c} LIKE ?" for c in search_cols)
                self._w(f'        if q:')
                self._w(f'            conditions.append("({or_clauses})")')
                for _ in search_cols:
                    self._w(f'            params.append(f"%{{q}}%")')

            self._w(f'        if conditions:')
            self._w(f'            query += " WHERE " + " AND ".join(conditions)')
            self._w(f'        cursor = await db.execute(query, params)')
            self._w(f'        rows = await cursor.fetchall()')
            self._w(f'        context["items"] = [dict(r) for r in rows]')
            self._w(f'        context["filters"] = dict(request.query_params)')

            # Populate distinct options for "distinct" type filters
            for ff in page.filters:
                if ff.filter_type == "distinct":
                    # Find which table has this column (may be a related table, not the display table)
                    distinct_table = table
                    for t in self.spec.tables:
                        if any(c.name == ff.key for c in t.columns) and t.name.snake != table:
                            distinct_table = t.name.snake
                            break
                    self._w(f'        wh_cursor = await db.execute("SELECT DISTINCT {ff.key} FROM {distinct_table} ORDER BY {ff.key}")')
                    self._w(f'        context["warehouse_options"] = [r["{ff.key}"] for r in await wh_cursor.fetchall()]')

            # Show related
            if page.related:
                related_table = page.related.related_table
                join_column = page.related.join_column
                display_cols = page.related.display_columns
                group_col = display_cols[0] if display_cols else "product"
                self._w(f'        for item in context["items"]:')
                self._w(f'            rcursor = await db.execute("SELECT * FROM {related_table} WHERE product = ?", (item["id"],))')
                self._w(f'            related_rows = [dict(r) for r in await rcursor.fetchall()]')
                self._w(f'            item["related"] = ", ".join(str(r.get("{group_col}", "")) + ":" + str(r.get("quantity", "")) for r in related_rows)')

        # Aggregations
        for agg in page.aggregations:
            slug_agg = agg.key
            if agg.agg_type == "count_by_status":
                tbl = agg.table
                self._w(f'        cursor_agg = await db.execute("SELECT COUNT(*) as total, status FROM {tbl} GROUP BY status")')
                self._w(f'        agg_rows = await cursor_agg.fetchall()')
                self._w("        agg_rows = [dict(r) for r in agg_rows]")
                self._w("        breakdown_parts = [str(r['status']) + ': ' + str(r['total']) for r in agg_rows]")
                self._w("        breakdown = ', '.join(breakdown_parts)")
                self._w("        total = sum(r['total'] for r in agg_rows)")
                self._w('        context["' + slug_agg + '"] = str(total) + " (" + breakdown + ")"')
            elif agg.agg_type == "count":
                tbl = agg.table
                self._w(f'        cursor_agg = await db.execute("SELECT COUNT(*) as cnt FROM {tbl}")')
                self._w(f'        context["{slug_agg}"] = (await cursor_agg.fetchone())["cnt"]')
            elif agg.agg_type == "sum_join":
                self._w(f'        cursor_agg = await db.execute("SELECT COALESCE(SUM(sl.quantity * p.unit_cost), 0) as val FROM stock_levels sl JOIN products p ON sl.product = p.id")')
                self._w("        val = (await cursor_agg.fetchone())['val']")
                self._w('        context["' + slug_agg + '"] = f"${val:.2f}"')

        # Nav badges
        for nav_item in self.spec.nav_items:
            if nav_item.badge_table:
                badge_slug = _snake(nav_item.label)
                badge_table = nav_item.badge_table
                self._w(f'        cursor_badge = await db.execute("SELECT COUNT(*) as cnt FROM {badge_table}")')
                self._w(f'        context["badge_{badge_slug}"] = (await cursor_badge.fetchone())["cnt"]')

        # Chart: data fetched via separate API
        if page.chart:
            self._w(f'        # Chart data is fetched via separate API endpoint')

        # Fetch reference lists for form dropdowns + existing records for CRUD table
        if page.form_fields:
            form_table = page.form_target_table
            if form_table:
                for ff in page.form_fields:
                    if ff.input_type == "reference" and ff.reference_table:
                        ref_table = ff.reference_table
                        self._w(f'        ref_cursor = await db.execute("SELECT * FROM {ref_table}")')
                        self._w(f'        context["{ref_table}_list"] = [dict(r) for r in await ref_cursor.fetchall()]')

                # Fetch existing records for the auto-generated CRUD table
                if not page.display_table:
                    self._w(f'        rows_cursor = await db.execute("SELECT * FROM {form_table} ORDER BY id DESC")')
                    self._w(f'        context["{form_table}_rows"] = [dict(r) for r in await rows_cursor.fetchall()]')

        # Generate jexl function registrations for termin.js runtime
        compute_js_parts = []
        for comp in self.spec.computes:
            if comp.body_lines and comp.input_params:
                param_name = comp.input_params[0].name if comp.input_params else "x"
                body_js = self._compile_compute_to_js(comp)
                compute_js_parts.append(
                    f'jexl.addFunction("{comp.name.display}", function({param_name}) {{ {body_js} }});'
                )
        self._w(f'        context["termin_compute_js"] = {repr(chr(10).join(compute_js_parts))}')

        self._w(f'        content_html = {var_name}.render(**context)')
        self._w(f'        return BASE_TEMPLATE.render(content=content_html, **context)')
        self._w(f'    finally:')
        self._w(f'        await db.close()')
        self._w()

        # POST route for form pages
        if page.form_fields and page.form_target_table:
            self._emit_form_post_route(page)

    def _emit_form_post_route(self, page: PageSpec) -> None:
        slug = page.slug
        form_table = page.form_target_table
        table_obj = self._table_by_snake.get(form_table)

        scope = page.required_scope
        scope_dep = f', user=Depends(require_scope("{scope}"))' if scope else ""

        # Determine initial state (only if table has a state machine)
        initial_state = page.create_as_status
        validate_field = page.validate_unique_field

        form_params = []
        for ff in page.form_fields:
            form_params.append(f'{ff.key}: str = Form("")')

        params_str = ", ".join(form_params)

        self._w(f'@app.post("/{slug}", response_class=HTMLResponse)')
        self._w(f"async def page_{slug}_post(request: Request, edit_id: str = Form(\"\"), {params_str}{scope_dep}):")
        self._w(f'    db = await get_db()')
        self._w(f'    try:')

        # Cast form string values to proper types
        for ff in page.form_fields:
            col = ff.key
            if ff.input_type == "number" or ff.input_type == "reference":
                self._w(f'        {col} = int({col}) if {col} else None')
            elif ff.input_type == "currency":
                self._w(f'        {col} = float({col}) if {col} else None')

        self._w(f'        if edit_id:')
        # UPDATE path
        update_cols = [ff.key for ff in page.form_fields]
        set_clause = ", ".join(f'{c} = ?' for c in update_cols)
        values_str_update = ", ".join(update_cols)
        self._w(f'            await db.execute(')
        self._w(f'                "UPDATE {form_table} SET {set_clause} WHERE id = ?",')
        self._w(f'                ({values_str_update}, int(edit_id),)')
        self._w(f'            )')
        self._w(f'        else:')

        # INSERT path
        insert_cols = [ff.key for ff in page.form_fields]
        if initial_state:
            insert_cols.append("status")
        cols_sql = ", ".join(insert_cols)
        placeholders = ", ".join(["?"] * len(insert_cols))
        values_list = [ff.key for ff in page.form_fields]
        if initial_state:
            values_list.append(f'"{initial_state}"')
        values_str = ", ".join(values_list)

        # Uniqueness validation (only on insert)
        if validate_field:
            vcol = validate_field
            self._w(f'            cursor = await db.execute("SELECT id FROM {form_table} WHERE {vcol} = ?", ({vcol},))')
            self._w(f'            if await cursor.fetchone():')
            # Find the display name for the field
            display_name = validate_field
            for ff in page.form_fields:
                if ff.key == validate_field:
                    display_name = ff.display
                    break
            self._w(f'                raise HTTPException(status_code=422, detail="{display_name} already exists")')

        self._w(f'            await db.execute(')
        self._w(f'                "INSERT INTO {form_table} ({cols_sql}) VALUES ({placeholders})",')
        self._w(f'                ({values_str},)')
        self._w(f'            )')
        self._w(f'        await db.commit()')
        self._w(f'        return RedirectResponse(url="/{slug}", status_code=303)')
        self._w(f'    finally:')
        self._w(f'        await db.close()')
        self._w()

        # DELETE route for CRUD table (only when page has no explicit display table)
        if not page.display_table:
            del_scope = (
                self._scope_for_verb(form_table, Verb.DELETE) or
                self._scope_for_verb(form_table, Verb.UPDATE) or
                page.required_scope
            )
            del_scope_dep = f', user=Depends(require_scope("{del_scope}"))' if del_scope else ""
            self._w(f'@app.post("/{slug}/delete/{{record_id}}", response_class=HTMLResponse)')
            self._w(f"async def page_{slug}_delete(record_id: int{del_scope_dep}):")
            self._w(f'    db = await get_db()')
            self._w(f'    try:')
            self._w(f'        await db.execute("DELETE FROM {form_table} WHERE id = ?", (record_id,))')
            self._w(f'        await db.commit()')
            self._w(f'        return RedirectResponse(url="/{slug}", status_code=303)')
            self._w(f'    finally:')
            self._w(f'        await db.close()')
            self._w()

    # ── Chart data API ──

    def _emit_chart_data_routes(self) -> None:
        for page in self.spec.pages:
            if page.chart:
                table = page.chart.table
                days = page.chart.days
                self._w(f'@app.get("/api/chart-data/{table}")')
                self._w(f"async def chart_data_{table}():")
                self._w(f'    db = await get_db()')
                self._w(f'    try:')
                sql = (f"SELECT DATE(created_at) as day, COUNT(*) as cnt "
                       f"FROM {table} "
                       f"WHERE created_at >= DATE('now', '-{days} days') "
                       f"GROUP BY DATE(created_at) ORDER BY day")
                self._w(f'        cursor = await db.execute("{sql}")')
                self._w(f'        rows = await cursor.fetchall()')
                self._w(f'        return {{"labels": [dict(r)["day"] for r in rows], "values": [dict(r)["cnt"] for r in rows]}}')
                self._w(f'    finally:')
                self._w(f'        await db.close()')
                self._w()

