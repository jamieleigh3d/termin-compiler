            cols = [c.name for c in table_obj.columns if not c.is_auto]
            placeholders = ", ".join("?" for _ in cols)
            col_names = ", ".join(cols)
            vals = ", ".join(f'body.get("{c}", "")' for c in cols)
            self._w(f'        await db.execute(')
