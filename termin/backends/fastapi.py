"""FastAPI backend: AppSpec (IR) -> single Python file (app.py).

Reads all data from the fully-resolved AppSpec intermediate representation.
No AST inference is needed -- every name, scope, column mapping, and
reference display column is already resolved in the IR.

Produces functionally equivalent output to the legacy codegen.py.
"""

import json
import re
import textwrap
from datetime import datetime, timezone
from typing import Optional

from ..ir import (
    AppSpec, Table, Column, ColumnType, AccessGrant, Verb,
    StateMachineSpec, TransitionSpec,
    EventSpec, EventConditionSpec, EventActionSpec,
    HttpMethod, RouteKind, RouteSpec,
    PageSpec, TableColumn, FilterField, FormField, HighlightRule,
    RelatedDataSpec, AggregationSpec, ChartSpec,
    NavItemSpec, StreamSpec, RoleSpec, AuthSpec,
    ComputeSpec, ComputeShape, ChannelSpec, ChannelDirection,
    ChannelDelivery, ChannelRequirementSpec, BoundarySpec,
    BoundaryPropertySpec,
)


def _snake(name: str) -> str:
    """Convert a display name to a Python/SQL identifier."""
    return re.sub(r'[^a-z0-9]+', '_', name.lower()).strip('_')


class FastApiBackend:
    """Reference FastAPI backend for the Termin compiler.

    Implements the Backend protocol from termin.backend.
    """

    name = "fastapi"

    def generate(self, spec: AppSpec, source_file: str = "") -> str:
        self.spec = spec
        self.lines: list[str] = []

        # Build lookup helpers from the IR
        self._table_by_snake: dict[str, Table] = {
            t.name.snake: t for t in spec.tables
        }
        self._sm_by_table: dict[str, StateMachineSpec] = {
            sm.table: sm for sm in spec.state_machines
        }
        self._grants_by_table: dict[str, list[AccessGrant]] = {}
        for g in spec.access_grants:
            self._grants_by_table.setdefault(g.table, []).append(g)

        self._emit_header(source_file)
        self._emit_imports()
        self._emit_expression_evaluator()
        self._emit_error_router()
        self._emit_app_setup()
        self._emit_database()
        self._emit_identity()
        self._emit_boundaries()
        self._emit_event_bus()
        self._emit_models()
        self._emit_state_machines()
        self._emit_event_handlers()
        self._emit_api_routes()
        self._emit_compute_routes()
        self._emit_compute_registrations()
        self._emit_channel_endpoints()
        self._emit_reflection_endpoint()
        self._emit_error_log_endpoint()
        self._emit_events_endpoint()
        self._emit_sse_stream()
        self._emit_templates()
        self._emit_page_routes()
        self._emit_startup()
        self._emit_main()
        return "\n".join(self.lines)

    def required_dependencies(self) -> list[str]:
        return [
            "fastapi>=0.100.0",
            "uvicorn>=0.23.0",
            "aiosqlite>=0.19.0",
            "jinja2>=3.1.0",
            "python-multipart>=0.0.6",
            "pyjexl>=0.3.0",
        ]

    # ── Helpers ──

    def _w(self, text: str = "") -> None:
        self.lines.append(text)

    def _wblock(self, text: str) -> None:
        self.lines.append(textwrap.dedent(text))

    def _scope_for_verb(self, table: str, verb: Verb) -> Optional[str]:
        for g in self._grants_by_table.get(table, []):
            if verb in g.verbs:
                return g.scope
        return None

    # ── Header ──

    def _emit_header(self, source_file: str) -> None:
        now = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")
        name = self.spec.name
        self._wblock(f'''\
            # ============================================================
            # GENERATED BY TERMIN COMPILER v0.1
            # Source: {source_file}
            # Generated: {now}
            # Application: {name}
            # DO NOT EDIT -- regenerate from .termin source
            # ============================================================
        ''')

