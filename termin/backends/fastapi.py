    def _gen_form_html(self, page: PageSpec) -> str:
        form_table = page.form_target_table
        if not form_table:
            return '<p>Form configuration error</p>'

        slug = page.slug

        parts = []
        parts.append(f'<form method="post" action="/{slug}" class="bg-white shadow rounded p-6 max-w-lg">')

        for ff in page.form_fields:
            col = ff.key
            display = ff.display

            if ff.input_type == "enum":
                parts.append(f'  <div class="mb-4">')
                parts.append(f'    <label class="block text-sm font-medium text-gray-700 mb-1">{display}</label>')
                parts.append(f'    <select name="{col}" class="w-full border rounded px-3 py-2">')
                parts.append(f'      <option value="">Select...</option>')
                for val in ff.enum_values:
                    parts.append(f'      <option value="{val}">{val}</option>')
                parts.append(f'    </select>')
                parts.append(f'  </div>')
                continue

            if ff.input_type == "reference":
                ref_table = ff.reference_table
                ref_display = ff.reference_display_col or "id"
                ref_unique = ff.reference_unique_col

                extra_attrs = ""
                if ff.required:
                    extra_attrs += " required"

                if ref_unique and ref_unique != ref_display:
                    option_label = f'{{{{ item.{ref_display} }}}} ({{{{ item.{ref_unique} }}}})'
                else:
                    option_label = f'{{{{ item.{ref_display} }}}}'

                # Find the display name of the referenced content
                ref_display_name = ref_table.replace("_", " ")
                # Try to find the actual content display name from tables
                ref_table_obj = self._table_by_snake.get(ref_table)
                if ref_table_obj:
                    ref_display_name = ref_table_obj.name.display

                parts.append(f'  <div class="mb-4">')
                parts.append(f'    <label class="block text-sm font-medium text-gray-700 mb-1">{display}</label>')
                parts.append(f'    <select name="{col}" class="w-full border rounded px-3 py-2"{extra_attrs}>')
                parts.append(f'      <option value="">Select {ref_display_name}...</option>')
                parts.append(f'      {{% for item in {ref_table}_list %}}')
                parts.append(f'      <option value="{{{{ item.id }}}}">{option_label}</option>')
                parts.append(f'      {{% endfor %}}')
                parts.append(f'    </select>')
                parts.append(f'  </div>')
                continue

            # Standard input types
            input_type = "text"
            extra_attrs = ""
            if ff.input_type == "currency":
                input_type = "number"
                extra_attrs = ' step="0.01"'
            elif ff.input_type == "number":
                input_type = "number"
                if ff.minimum is not None:
                    extra_attrs = f' min="{ff.minimum}"'

            if ff.required:
                extra_attrs += " required"

            parts.append(f'  <div class="mb-4">')
            parts.append(f'    <label class="block text-sm font-medium text-gray-700 mb-1">{display}</label>')
            parts.append(f'    <input type="{input_type}" name="{col}" class="w-full border rounded px-3 py-2"{extra_attrs}>')
            parts.append(f'  </div>')

        parts.append(f'  <input type="hidden" name="edit_id" value="">')
        parts.append(f'  <button type="submit" class="bg-indigo-600 text-white px-6 py-2 rounded hover:bg-indigo-700">Save</button>')
        parts.append(f'</form>')

        # Auto-generate CRUD table if page has no explicit display table
        if not page.display_table:
            table_obj = self._table_by_snake.get(form_table)
            if table_obj:
                display_cols = ["id"] + [ff.key for ff in page.form_fields]
                if table_obj.has_status_column and "status" not in display_cols:
                    display_cols.append("status")

                # Add automatic timestamp columns
                for col_def in table_obj.columns:
                    if col_def.is_auto and col_def.name not in display_cols:
                        display_cols.append(col_def.name)

                parts.append(f'<h2 class="text-xl font-semibold mt-8 mb-4">Existing {table_obj.name.display.title()}</h2>')
                parts.append(f'<div class="w-full bg-white shadow rounded overflow-hidden">')
                parts.append(f'<table class="w-full">')
                parts.append(f'  <thead class="bg-gray-100">')
                parts.append(f'    <tr>')
                for col_name in display_cols:
                    parts.append(f'      <th class="px-4 py-2 text-left text-sm font-medium text-gray-600">{col_name.replace("_", " ")}</th>')
                parts.append(f'      <th class="px-4 py-2 text-left text-sm font-medium text-gray-600">actions</th>')
                parts.append(f'    </tr>')
                parts.append(f'  </thead>')
                parts.append(f'  <tbody>')
                parts.append(f'    {{% for row in {form_table}_rows %}}')
                parts.append(f'    <tr class="border-t">')
                for col_name in display_cols:
                    parts.append(f'      <td class="px-4 py-2 text-sm">{{{{ row.{col_name}|default("") }}}}</td>')
                # Edit button JS
                js_fill_parts = []
                for ff in page.form_fields:
                    col_name = ff.key
                    js_fill_parts.append(f"""el=document.querySelector('[name="{col_name}"]');if(el){{if(el.tagName==="SELECT"){{for(var i=0;i<el.options.length;i++)if(el.options[i].value==row.{col_name})el.selectedIndex=i;}}else el.value=row.{col_name};}}""")
                js_fill = "".join(js_fill_parts)
                js_edit = f"var row={{{{row|tojson}}}};document.querySelector('[name=\\'edit_id\\']').value=row.id;{js_fill}window.scrollTo(0,0);return false;"
                parts.append(f'      <td class="px-4 py-2 text-sm">')
                parts.append(f'        <a href="#" onclick=\'{js_edit}\' class="text-indigo-600 hover:underline mr-3">Edit</a>')
                parts.append(f'        <form method="post" action="/{slug}/delete/{{{{ row.id }}}}" class="inline" onsubmit="return confirm(\'Delete this record?\')">')
                parts.append(f'          <button type="submit" class="text-red-600 hover:underline">Delete</button>')
                parts.append(f'        </form>')
                parts.append(f'      </td>')
                parts.append(f'    </tr>')
                parts.append(f'    {{% endfor %}}')
                parts.append(f'  </tbody>')
                parts.append(f'</table>')
                parts.append(f'</div>')

        return "\n".join(parts)

